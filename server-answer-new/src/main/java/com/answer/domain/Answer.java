package com.answer.domain;

public class Answer
{
  private long ansID;
  private String ansDesc;
  
  public long getAnsID()
  {
    return this.ansID;
  }
  
  public void setAnsID(long ansID)
  {
    this.ansID = ansID;
  }
  
  public String getAnsDesc()
  {
    return this.ansDesc;
  }
  
  public void setAnsDesc(String ansDesc)
  {
    this.ansDesc = ansDesc;
  }
}
